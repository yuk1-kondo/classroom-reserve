// „É™„Éï„Ç°„ÇØ„Çø„É™„É≥„Ç∞Áâà„Çµ„Ç§„Éâ„Éë„Éç„É´„Ç≥„É≥„Éù„Éº„Éç„É≥„Éà - ‰∫àÁ¥Ñ‰ΩúÊàê„ÉªË°®Á§∫Áî®
import React, { useEffect, useState } from 'react';
import { UserSection } from './UserSection';
import { ReservationForm } from './ReservationForm';
import SimpleLogin from './SimpleLogin';
import { useReservationData } from '../hooks/useReservationData';
import { useAuth } from '../hooks/useAuth';
import { useReservationForm } from '../hooks/useReservationForm';
import { useConflictDetection } from '../hooks/useConflictDetection';
import { reservationsService } from '../firebase/firestore';
import { Timestamp } from 'firebase/firestore';
import './SidePanel.css';

interface SidePanelProps {
  selectedDate?: string;
  selectedEventId?: string;
  onClose?: () => void;
  onReservationCreated?: () => void;
}

export const SidePanel: React.FC<SidePanelProps> = ({
  selectedDate,
  selectedEventId,
  onClose,
  onReservationCreated
}) => {
  // „Ç´„Çπ„Çø„É†„Éï„ÉÉ„ÇØ„ÅßÁä∂ÊÖãÁÆ°ÁêÜ„ÇíÂàÜÈõ¢
  const { currentUser, showLoginModal, setShowLoginModal, handleLoginSuccess, handleLogout } = useAuth();
  const { rooms, reservations } = useReservationData(currentUser, selectedDate);
  const formHook = useReservationForm(selectedDate, currentUser, rooms, onReservationCreated);
  const { conflictCheck, performConflictCheck } = useConflictDetection();
  
  // ÂøÖË¶Å„Å™ÂÄ§/Èñ¢Êï∞„Å†„ÅëÂàÜËß£ÔºàuseEffect‰æùÂ≠ò„ÅÆÂÆâÂÆöÂåñÔºâ
  const { showForm, formData, getReservationDates, getReservationPeriods } = formHook;
  const { selectedRoom } = formData;
  
  // ÁÆ°ÁêÜËÄÖÊ©üËÉΩ„ÅÆË°®Á§∫Áä∂ÊÖã
  const [csvExporting, setCsvExporting] = useState(false);
  const [csvImporting, setCsvImporting] = useState(false);
  const [bulkDeleting, setBulkDeleting] = useState(false);
  const [csvMessage, setCsvMessage] = useState('');
  
  // ÁÆ°ÁêÜËÄÖÊ®©Èôê„ÉÅ„Çß„ÉÉ„ÇØ
  const isAdmin = currentUser?.role === 'admin' || currentUser?.isAdmin;

  // CSV „Ç®„ÇØ„Çπ„Éù„Éº„ÉàÊ©üËÉΩ
  const handleCsvExport = async () => {
    if (!isAdmin) return;
    
    setCsvExporting(true);
    setCsvMessage('‰∫àÁ¥Ñ„Éá„Éº„Çø„ÇíÂèñÂæó‰∏≠...');
    
    try {
      const endDate = new Date();
      const startDate = new Date();
      startDate.setFullYear(startDate.getFullYear() - 1);
      
      const reservations = await reservationsService.getReservations(startDate, endDate);
      
      if (reservations.length === 0) {
        setCsvMessage('„Ç®„ÇØ„Çπ„Éù„Éº„Éà„Åô„Çã‰∫àÁ¥Ñ„Éá„Éº„Çø„Åå„ÅÇ„Çä„Åæ„Åõ„Çì');
        setTimeout(() => setCsvMessage(''), 3000);
        return;
      }
      
      // Á∞°Âçò„Å™CSVÂΩ¢Âºè„Åß„Ç®„ÇØ„Çπ„Éù„Éº„Éà
      const csvHeaders = ['Êó•‰ªò', 'ÊôÇÈñì', 'ÊïôÂÆ§', '‰∫àÁ¥ÑÂêç', '‰∫àÁ¥ÑËÄÖ'];
      const csvData = reservations.map(reservation => [
        reservation.startTime.toDate().toLocaleDateString('ja-JP'),
        reservation.periodName || reservation.period,
        reservation.roomName || reservation.roomId,
        reservation.reservationName || reservation.title,
        reservation.createdBy || '‰∏çÊòé'
      ]);
      
      const csvContent = [csvHeaders, ...csvData]
        .map(row => row.map(cell => `"${cell}"`).join(','))
        .join('\n');
      
      const blob = new Blob(['\uFEFF' + csvContent], { type: 'text/csv;charset=utf-8;' });
      const link = document.createElement('a');
      const url = URL.createObjectURL(blob);
      link.setAttribute('href', url);
      link.setAttribute('download', `reservations_${new Date().toISOString().slice(0,10)}.csv`);
      link.style.visibility = 'hidden';
      document.body.appendChild(link);
      link.click();
      document.body.removeChild(link);
      
      setCsvMessage(`‚úÖ ${reservations.length}‰ª∂„ÅÆ‰∫àÁ¥Ñ„Éá„Éº„Çø„Çí„Ç®„ÇØ„Çπ„Éù„Éº„Éà„Åó„Åæ„Åó„Åü`);
      setTimeout(() => setCsvMessage(''), 3000);
      
    } catch (error) {
      console.error('CSV export error:', error);
      setCsvMessage('‚ùå „Ç®„ÇØ„Çπ„Éù„Éº„Éà„Å´Â§±Êïó„Åó„Åæ„Åó„Åü');
      setTimeout(() => setCsvMessage(''), 3000);
    } finally {
      setCsvExporting(false);
    }
  };

  // CSV „Ç§„É≥„Éù„Éº„ÉàÊ©üËÉΩ
  const handleCsvImport = async (event: React.ChangeEvent<HTMLInputElement>) => {
    const file = event.target.files?.[0];
    if (!file || !isAdmin) return;

    const confirmed = window.confirm('‚ö†Ô∏è CSV„Éï„Ç°„Ç§„É´„Åã„Çâ‰∫àÁ¥Ñ„Éá„Éº„Çø„Çí„Ç§„É≥„Éù„Éº„Éà„Åó„Åæ„Åô„ÄÇ„Çà„Çç„Åó„ÅÑ„Åß„Åô„ÅãÔºü');
    if (!confirmed) {
      event.target.value = '';
      return;
    }

    setCsvImporting(true);
    setCsvMessage('CSV„Éï„Ç°„Ç§„É´„ÇíË™≠„ÅøËæº„Åø‰∏≠...');

    try {
      const text = await file.text();
      const lines = text.split('\n').filter(line => line.trim());
      
      if (lines.length <= 1) {
        setCsvMessage('‚ùå CSV„Éï„Ç°„Ç§„É´„Å´„Éá„Éº„Çø„ÅåÂê´„Åæ„Çå„Å¶„ÅÑ„Åæ„Åõ„Çì');
        return;
      }

      // „Éò„ÉÉ„ÉÄ„ÉºË°å„Çí„Çπ„Ç≠„ÉÉ„Éó
      const dataLines = lines.slice(1);
      let successCount = 0;
      let failedCount = 0;

      setCsvMessage(`${dataLines.length}‰ª∂„ÅÆ‰∫àÁ¥Ñ„Éá„Éº„Çø„Çí„Ç§„É≥„Éù„Éº„Éà‰∏≠...`);

      for (const line of dataLines) {
        try {
          const columns = line.split(',').map(col => col.replace(/"/g, '').trim());
          
          if (columns.length < 5) continue;

          const [dateStr, timeStr, roomId, reservationName, createdBy] = columns;
          
          // Êó•‰ªòËß£Êûê
          const dateParts = dateStr.split('/');
          if (dateParts.length !== 3) continue;
          
          const year = parseInt(dateParts[0]);
          const month = parseInt(dateParts[1]) - 1;
          const day = parseInt(dateParts[2]);
          
          // ÊôÇÈñìËß£ÊûêÔºà"1ÊôÇÈñìÁõÆ" -> "1"Ôºâ
          const period = timeStr.replace(/ÊôÇÈñìÁõÆ/, '');
          
          const startDate = new Date(year, month, day, 9 + parseInt(period) - 1, 0);
          const endDate = new Date(year, month, day, 10 + parseInt(period) - 1, 0);

          const reservation = {
            roomId: roomId,
            roomName: roomId,
            title: reservationName,
            reservationName: reservationName,
            startTime: Timestamp.fromDate(startDate),
            endTime: Timestamp.fromDate(endDate),
            period: period,
            periodName: `${period}ÊôÇÈñìÁõÆ`,
            createdBy: createdBy || 'CSV import'
          };

          await reservationsService.addReservation(reservation);
          successCount++;
        } catch (error) {
          console.error('‰∫àÁ¥Ñ„Ç§„É≥„Éù„Éº„Éà„Ç®„É©„Éº:', error);
          failedCount++;
        }
      }

      setCsvMessage(`‚úÖ „Ç§„É≥„Éù„Éº„ÉàÂÆå‰∫Ü: ÊàêÂäü ${successCount}‰ª∂ / Â§±Êïó ${failedCount}‰ª∂`);
      setTimeout(() => setCsvMessage(''), 5000);

    } catch (error) {
      console.error('CSV import error:', error);
      setCsvMessage('‚ùå CSV„Éï„Ç°„Ç§„É´„ÅÆË™≠„ÅøËæº„Åø„Å´Â§±Êïó„Åó„Åæ„Åó„Åü');
      setTimeout(() => setCsvMessage(''), 3000);
    } finally {
      setCsvImporting(false);
      event.target.value = '';
    }
  };

  // ‰∏ÄÊã¨ÂâäÈô§Ê©üËÉΩ
  const handleBulkDelete = async () => {
    if (!isAdmin) return;

    const confirmed = window.confirm('‚ö†Ô∏è Ë≠¶Âëä: „Åô„Åπ„Å¶„ÅÆ‰∫àÁ¥Ñ„Éá„Éº„Çø„ÇíÂâäÈô§„Åó„Åæ„Åô„ÄÇ„Åì„ÅÆÊìç‰Ωú„ÅØÂèñ„ÇäÊ∂à„Åõ„Åæ„Åõ„Çì„ÄÇÊú¨ÂΩì„Å´ÂÆüË°å„Åó„Åæ„Åô„ÅãÔºü');
    if (!confirmed) return;

    const doubleConfirmed = window.confirm('üö® ÊúÄÁµÇÁ¢∫Ë™ç: Êú¨ÂΩì„Å´„Åô„Åπ„Å¶„ÅÆ‰∫àÁ¥Ñ„Éá„Éº„Çø„ÇíÂâäÈô§„Åó„Åæ„Åô„ÅãÔºü');
    if (!doubleConfirmed) return;

    setBulkDeleting(true);
    setCsvMessage('‰∫àÁ¥Ñ„Éá„Éº„Çø„ÇíÂèñÂæó‰∏≠...');

    try {
      const endDate = new Date();
      const startDate = new Date();
      startDate.setFullYear(startDate.getFullYear() - 10);
      
      const reservations = await reservationsService.getReservations(startDate, endDate);
      
      if (reservations.length === 0) {
        setCsvMessage('ÂâäÈô§„Åô„Çã‰∫àÁ¥Ñ„Éá„Éº„Çø„Åå„ÅÇ„Çä„Åæ„Åõ„Çì');
        setTimeout(() => setCsvMessage(''), 3000);
        return;
      }

      setCsvMessage(`${reservations.length}‰ª∂„ÅÆ‰∫àÁ¥Ñ„Éá„Éº„Çø„ÇíÂâäÈô§‰∏≠...`);
      
      let deletedCount = 0;
      for (const reservation of reservations) {
        if (reservation.id) {
          await reservationsService.deleteReservation(reservation.id);
          deletedCount++;
        }
      }

      setCsvMessage(`‚úÖ ${deletedCount}‰ª∂„ÅÆ‰∫àÁ¥Ñ„Éá„Éº„Çø„ÇíÂâäÈô§„Åó„Åæ„Åó„Åü`);
      setTimeout(() => setCsvMessage(''), 5000);

    } catch (error) {
      console.error('Bulk delete error:', error);
      setCsvMessage('‚ùå ‰∏ÄÊã¨ÂâäÈô§„Å´Â§±Êïó„Åó„Åæ„Åó„Åü');
      setTimeout(() => setCsvMessage(''), 3000);
    } finally {
      setBulkDeleting(false);
    }
  };

  // ÈáçË§á„ÉÅ„Çß„ÉÉ„ÇØ„ÇíÂÆüË°å„Åô„Çã„Åü„ÇÅ„ÅÆ„Ç®„Éï„Çß„ÇØ„Éà
  useEffect(() => {
    if (showForm) {
      const timeoutId = setTimeout(() => {
        const datesToCheck = getReservationDates();
        const periodsToCheck = getReservationPeriods();
        
        if (datesToCheck.length > 0 && periodsToCheck.length > 0 && selectedRoom) {
          performConflictCheck(datesToCheck, periodsToCheck, selectedRoom);
        }
      }, 300); // „Éá„Éê„Ç¶„É≥„Çπ: 300msÂæÖ„Å£„Å¶„Åã„Çâ„ÉÅ„Çß„ÉÉ„ÇØÂÆüË°å
      
      return () => clearTimeout(timeoutId);
    }
  }, [showForm, selectedRoom, getReservationDates, getReservationPeriods, performConflictCheck]);

  // „É≠„Ç∞„Ç§„É≥„É¢„Éº„ÉÄ„É´: ESC„ÅßÈñâ„Åò„Çã
  useEffect(() => {
    if (!showLoginModal) return;
    const onKeyDown = (e: KeyboardEvent) => {
      if (e.key === 'Escape') {
        setShowLoginModal(false);
      }
    };
    window.addEventListener('keydown', onKeyDown);
    return () => window.removeEventListener('keydown', onKeyDown);
  }, [showLoginModal, setShowLoginModal]);

  // Êó•‰ªò„Éï„Ç©„Éº„Éû„ÉÉ„Éà
  const formatDate = (dateStr: string): string => {
    const date = new Date(dateStr);
    return date.toLocaleDateString('ja-JP', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
      weekday: 'long'
    });
  };

  return (
    <div className="side-panel">
      <div className="only-mobile mobile-inline-close-wrapper">
        <button onClick={onClose} aria-label="Èñâ„Åò„Çã" className="mobile-inline-close-btn">‚úï Èñâ„Åò„Çã</button>
      </div>
      {/* „É¶„Éº„Ç∂„ÉºÊÉÖÂ†±„Çª„ÇØ„Ç∑„Éß„É≥ */}
      <UserSection
        currentUser={currentUser}
        onLogin={() => setShowLoginModal(true)}
        onLogout={handleLogout}
      />

      <div className="side-panel-header">
        <h3>üìÖ ‰∫àÁ¥ÑÁÆ°ÁêÜ</h3>
      </div>

      {selectedDate ? (
        <div className="side-panel-content">
          <div className="selected-date">
            <h4>{formatDate(selectedDate)}</h4>
          </div>

          {/* ‰∫àÁ¥Ñ‰ΩúÊàê„Éï„Ç©„Éº„É† */}
          <ReservationForm
            showForm={formHook.showForm}
            onShowForm={formHook.setShowForm}
            loading={formHook.loading}
            currentUser={currentUser}
            formData={formHook.formData}
            updateFormData={formHook.updateFormData}
            dateRange={formHook.dateRange}
            setDateRange={formHook.setDateRange}
            periodRange={formHook.periodRange}
            setPeriodRange={formHook.setPeriodRange}
            rooms={rooms}
            conflictCheck={conflictCheck}
            onCreateReservation={formHook.handleCreateReservation}
            reservations={reservations}
            selectedDate={selectedDate}
          />

          {/* ÁÆ°ÁêÜËÄÖÊ©üËÉΩ„Çª„ÇØ„Ç∑„Éß„É≥ */}
          {isAdmin && (
            <div className="admin-section">
              <h4>üîß ÁÆ°ÁêÜËÄÖÊ©üËÉΩ</h4>
              {csvMessage && (
                <div className={`csv-message ${csvMessage.includes('‚ùå') ? 'error' : 'success'}`}>
                  {csvMessage}
                </div>
              )}
              <div className="admin-functions">
                <button 
                  onClick={handleCsvExport}
                  disabled={csvExporting || csvImporting || bulkDeleting}
                  className="admin-btn csv-btn"
                >
                  üìÑ {csvExporting ? '„Ç®„ÇØ„Çπ„Éù„Éº„Éà‰∏≠...' : 'CSVÂá∫Âäõ'}
                </button>
                
                <label className="admin-btn import-btn">
                  üì• {csvImporting ? '„Ç§„É≥„Éù„Éº„Éà‰∏≠...' : 'CSVÂÖ•Âäõ'}
                  <input
                    type="file"
                    accept=".csv"
                    onChange={handleCsvImport}
                    disabled={csvExporting || csvImporting || bulkDeleting}
                    className="hidden-file-input"
                  />
                </label>
                
                <button 
                  onClick={handleBulkDelete}
                  disabled={csvExporting || csvImporting || bulkDeleting}
                  className="admin-btn delete-btn"
                >
                  üóëÔ∏è {bulkDeleting ? 'ÂâäÈô§‰∏≠...' : '‰∏ÄÊã¨ÂâäÈô§'}
                </button>
              </div>
            </div>
          )}

          {/* ÂÆüÁî®ÁöÑ„Å™ÈÅãÁî®Ê°àÂÜÖ„É°„ÉÉ„Çª„Éº„Ç∏ */}
          <div className="info-message">
            <p>‚ö†Ô∏è ÊïôÂÆ§‰∫àÁ¥ÑÊ∏à„Åø„ÅÆÂ†¥Âêà„ÅØÂÖàÁîüÈñì„ÅßÁõ∏Ë´á„Åó„Å¶Â§âÊõ¥„Åó„Å¶‰∏ã„Åï„ÅÑ</p>
          </div>
        </div>
      ) : (
        <div className="no-date-selected">
          <p>üìÖ „Ç´„É¨„É≥„ÉÄ„Éº„Åã„ÇâÊó•‰ªò„Çí„ÇØ„É™„ÉÉ„ÇØ„Åó„Å¶‰∫àÁ¥Ñ„ÇíÁÆ°ÁêÜ„Åó„Å¶„Åè„Å†„Åï„ÅÑ</p>
        </div>
      )}
      
      {/* „É≠„Ç∞„Ç§„É≥„É¢„Éº„ÉÄ„É´ */}
      {showLoginModal && (
        <div className="modal-overlay" onClick={() => setShowLoginModal(false)}>
          <div className="modal-content" onClick={(e) => e.stopPropagation()}>
            <SimpleLogin
              onAuthStateChange={handleLoginSuccess}
            />
            <button 
              className="modal-close-btn"
              onClick={() => setShowLoginModal(false)}
            >
              ‚úï
            </button>
          </div>
        </div>
      )}
    </div>
  );
};

export default SidePanel;
